    private void btnPalpiteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPalpiteActionPerformed
        // TODO add your handling code here:
        Timer timer = new Timer(1000, new ActionListener() {
      @Override
      public void actionPerformed(ActionEvent arg0) {
        // do your thing here
        double n = 1 + Math.random() * (6-1);
        int valor = (int) n;
        int palpite = Integer.parseInt(txtValor.getValue().toString());
        if (valor == palpite){
            lblFrase.setFont(new Font("Arial Black", Font.PLAIN, 18));
            lblFrase.setText("<html><b>Acertou!</b></html>");
            akinatorImagem.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/akinatorAcertou.png")));
        } else{
            lblFrase.setText("<html>Errou. Tente novamente!<br> Eu pensei no valor " + valor + "</html>" );
            akinatorImagem.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/akinatorErrou.png")));
            }
      }
    });
    timer.setRepeats(false); 
    lblFrase.setText("Deixe-me ver...");
    lblFrase.setFont(new Font("Segoe UI", Font.PLAIN, 14));
    akinatorImagem.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/akinatorPensando.png")));
    timer.start(); 
    }//GEN-LAST:event_btnPalpiteActionPerformed

